package com.bean;

public class StudentBean {
	
	private String std_name,std_address,std_dob,std_mobileno,email,password,status;
	
	

	private int std_id;

	public String getStd_name() {
		return std_name;
	}

	public void setStd_name(String std_name) {
		this.std_name = std_name;
	}

	public String getStd_address() {
		return std_address;
	}

	public void setStd_address(String std_address) {
		this.std_address = std_address;
	}

	public String getStd_dob() {
		return std_dob;
	}

	public void setStd_dob(String std_dob) {
		this.std_dob = std_dob;
	}

	public String getStd_mobileno() {
		return std_mobileno;
	}

	public void setStd_mobileno(String std_mobileno) {
		this.std_mobileno = std_mobileno;
	}

	
	
	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public int getStd_id() {
		return std_id;
	}

	public void setStd_id(int std_id) {
		this.std_id = std_id;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	

}
